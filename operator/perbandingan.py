
nilai1 = int(input("Nilai 1 : "))
nilai2 = int(input("Nilai 2 : "))
print("Nilai 1 : ", nilai1)
print("Nilai 2 : ", nilai2)

#operator kurang dari <
kurangdari = nilai1 < nilai2
print(nilai1, "<" ,nilai2,"=", kurangdari)

#operator lebih dari >
lebihdari = nilai1 > nilai2
print(nilai1, ">" ,nilai2,"=", lebihdari)

#operator kurang dari sama dengan <=
hasil = nilai1 <= nilai2
print(nilai1, "<=" ,nilai2,"=", hasil)

#operator lebih dari sama dengan >=
hasil = nilai1 >= nilai2
print(nilai1, ">=" ,nilai2,"=", hasil)

#operator sama dengan sama dengan ==
hasil = nilai1 == nilai2
print(nilai1, "==" ,nilai2,"=", hasil)

#operator tidak sama dengan !=
hasil = nilai1 != nilai2
print(nilai1, "!=" ,nilai2,"=", hasil)