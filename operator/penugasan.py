#operasi penugasan
nilai1 = 5
print("Nilai 1 adalah", nilai1)

nilai1 += 2
print("Nilai 1 + 2 menjadi", nilai1)

nilai1 -= 3
print("Nilai 1 - 3 menjadi", nilai1)

nilai1 *= 2
print("Nilai 1 * 2 menjadi", nilai1)

nilai1 /= 3
print("Nilai 1 / 3 menjadi", nilai1)

nilai1 **= 3
print("Nilai 1 ** 3 menjadi", nilai1)

nilai1 //= 2
print("Nilai 1 // 2 menjadi", nilai1)

nilai1 %= 2
print("Nilai 1 % 2 menjadi", nilai1)