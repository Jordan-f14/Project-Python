#operator aritmatika 

#operator penjumlahan
nilai1 = 6
nilai2 = 4
hasil = nilai1 + nilai2
print("=================Penjumlahan=================")
print(nilai1, "+" , nilai2, "=" ,hasil)

#operator pengurangan
hasil = nilai1 - nilai2
print("=================Pengurangan=================")
print(nilai1, "-" , nilai2, "=" ,hasil)

#operator perkalian
hasil = nilai1 * nilai2
print("=================Perkalian===================")
print(nilai1, "*" , nilai2, "=" ,hasil)

#operator pembagian 
hasil = nilai1 / nilai2
print("=================Pemabagian==================")
print(nilai1, "/" , nilai2, "=" ,hasil)

#operator pemangkatan 
hasil = nilai1 ** nilai2
print("=================Pemangkatan=================")
print(nilai1, "**" , nilai2, "=" ,hasil)

#operator pembagian bulat
hasil = nilai1 // nilai2
print("=================Pembagian Bulat=================")
print(nilai1, "//" , nilai2, "=" ,hasil)

#operator pembagian bulat
hasil = nilai1 % nilai2
print("=================Pembagian Bulat=================")
print(nilai1, "%" , nilai2, "=" ,hasil)

